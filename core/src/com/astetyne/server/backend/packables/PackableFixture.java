package com.astetyne.server.backend.packables;

public class PackableFixture {

    public int id;
    public float x1, y1, x2, y2;

    public PackableFixture(int id, float x1, float y1, float x2, float y2) {
        this.id = id;
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }
}
